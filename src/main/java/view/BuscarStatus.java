    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<Despesa> despesas = despesaBO.buscarPorStatus(jComboBox1.getSelectedItem().toString());
        
        DefaultTableModel dtmDespesas = (DefaultTableModel) jTable1.getModel();

        dtmDespesas.setRowCount(0);

        for (Despesa despesa : despesas) {
            Object[] linha = {despesa.getId(), despesa.getNome(), despesa.getPreco(), despesa.getStatus(), despesa.getDataVencimento()};
            dtmDespesas.addRow(linha);
        }
        
        double total = 0;
        
        for (Despesa despesa : despesas){
            if (despesa.getStatus().equals("Pendente")) {
                total += despesa.getPreco();
            }
            
            if (despesa.getStatus().equals("Pago")) {
                total += despesa.getPreco();
            }
        }
        
         txtTotal.setText("R$ " + String.format("%.2f", total));
    }//GEN-LAST:event_jButton1ActionPerformed

